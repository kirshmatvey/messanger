import React, { ChangeEvent, KeyboardEvent, useState } from 'react'
import Greeting from './Greeting'
import { UserType } from './HW3'

type GreetingContainerPropsType = {
    users: UserType[] // need to fix any
    addUserCallback: (name: UserType['name']) => void // need to fix any
}

export const pureAddUser = (name: UserType['name'], setError: (newError: string) => void, setName: (newName: UserType['name']) => void, addUserCallback: GreetingContainerPropsType['addUserCallback']) => {
    if (name.trim() === '') {
        setError('Ошибка! Введите имя!')
    } else {
        addUserCallback(name)
        setName('')
    }
    // если имя пустое - показать ошибку, иначе - добавить юзера и очистить инпут
}

export const pureOnBlur = (name: UserType['name'], setError: (newError: string) => void) => {
    name.trim() === '' && setError('Ошибка! Введите имя!')
    // если имя пустое - показать ошибку
}

export const pureOnEnter = (e: KeyboardEvent<HTMLInputElement>, addUser: () => void) => {
    if (e.key === 'Enter') {
        addUser()
    }// если нажата кнопка Enter - добавить
}

